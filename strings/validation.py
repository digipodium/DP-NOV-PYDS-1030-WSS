a =  input("Enter anything: ")
if a.isalpha():
    print("You entered Alphabets")
if a.isalnum():
    print("You entered Alphanumeric")
if a.isdigit():
    print("You entered Digits")
if a.isnumeric():
    print("You entered Numbers")
if a.islower():
    print("You entered Lowercase")
if a.isupper():
    print("You entered Uppercase")
